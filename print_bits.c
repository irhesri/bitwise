# include "bitwise.h"

void	print_bits(unsigned char u)
{
	int	i;
	int	b;

	i = 8;
	while (i--)
	{
		b = ((u & 128) != 0) + 48;
		write(1, &b, 1);
		u <<= 1;
	}
}
